const fs = require('fs');
const path = require('path');
const vm = require('vm');

const ROOT = path.join(__dirname, '..');
const SOURCE_PATH = path.join(ROOT, 'js', 'results_extra_players.js');
const OUTPUT_PATH = path.join(ROOT, 'js', 'results_extra_players_ranked.js');
const MIN_MATCHES = 20;

function loadWindowVar(filePath, expression) {
  const context = { console, window: {} };
  context.globalThis = context;
  const code = fs.readFileSync(filePath, 'utf8') + `\n;globalThis.__OUT__ = ${expression};`;
  vm.runInNewContext(code, context, { filename: path.basename(filePath) });
  return context.__OUT__;
}

function rankPlayers(players, metric, minMatches = MIN_MATCHES) {
  return players
    .filter((player) => player.overall.matches >= minMatches && player.overall[metric] != null)
    .sort((a, b) => {
      return b.overall[metric] - a.overall[metric]
        || b.overall.matches - a.overall.matches
        || a.name.localeCompare(b.name);
    })
    .map((player, index) => ({
      rank: index + 1,
      id: player.id,
      name: player.name,
      flag: player.flag,
      country: player.country,
      era: player.era,
      matches: player.overall.matches,
      winPct: player.overall.winPct,
      metric,
      value: player.overall[metric],
      controlIndex100: player.overall.controlIndex100,
      servePointsWonPct: player.overall.servePointsWonPct,
      returnPointsWonPct: player.overall.returnPointsWonPct,
      holdPct: player.overall.holdPct,
      breakPointsConvertedPct: player.overall.breakPointsConvertedPct,
      continueAfterTwoWinsPct: player.overall.continueAfterTwoWinsPct,
      recoverAfterTwoLossesPct: player.overall.recoverAfterTwoLossesPct,
      matchWinStreak3Pct: player.overall.matchWinStreak3Pct,
      matchLossStreak3Pct: player.overall.matchLossStreak3Pct
    }));
}

function buildRankings(data) {
  const players = data.players;
  return {
    gen: new Date().toISOString().slice(0, 10),
    source: 'results_extra_players.js',
    minMatches: MIN_MATCHES,
    totalPlayers: players.length,
    eligiblePlayers: players.filter((player) => player.overall.matches >= MIN_MATCHES).length,
    leaderboards: {
      controlIndex100: rankPlayers(players, 'controlIndex100'),
      servePointsWonPct: rankPlayers(players, 'servePointsWonPct'),
      returnPointsWonPct: rankPlayers(players, 'returnPointsWonPct'),
      holdPct: rankPlayers(players, 'holdPct'),
      breakPointsConvertedPct: rankPlayers(players, 'breakPointsConvertedPct'),
      continueAfterTwoWinsPct: rankPlayers(players, 'continueAfterTwoWinsPct'),
      recoverAfterTwoLossesPct: rankPlayers(players, 'recoverAfterTwoLossesPct'),
      matchWinStreak3Pct: rankPlayers(players, 'matchWinStreak3Pct'),
      lowestMatchLossStreak3Pct: players
        .filter((player) => player.overall.matches >= MIN_MATCHES && player.overall.matchLossStreak3Pct != null)
        .sort((a, b) => {
          return a.overall.matchLossStreak3Pct - b.overall.matchLossStreak3Pct
            || b.overall.matches - a.overall.matches
            || a.name.localeCompare(b.name);
        })
        .map((player, index) => ({
          rank: index + 1,
          id: player.id,
          name: player.name,
          flag: player.flag,
          country: player.country,
          era: player.era,
          matches: player.overall.matches,
          metric: 'matchLossStreak3Pct',
          value: player.overall.matchLossStreak3Pct,
          controlIndex100: player.overall.controlIndex100
        }))
    }
  };
}

function writeOutput(data) {
  const banner = [
    '// Auto-generated by tools/build_results_extra_players_ranked.js',
    `// Source: ${data.source}`,
    `// Eligible players: ${data.eligiblePlayers}`
  ].join('\n');

  const body = `window.RESULTS_EXTRA_PLAYERS_RANKED = ${JSON.stringify(data)};\n`;
  fs.writeFileSync(OUTPUT_PATH, `${banner}\n\n${body}`, 'utf8');
}

function main() {
  const data = loadWindowVar(SOURCE_PATH, 'window.RESULTS_EXTRA_PLAYERS');
  const rankings = buildRankings(data);
  writeOutput(rankings);
  console.log(`Wrote ${OUTPUT_PATH}`);
}

main();
